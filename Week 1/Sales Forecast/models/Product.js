import mongoose, { Schema } from "mongoose";

const ProductSchema = Schema({
  name: {
    type: String,
    required: true,
    unique: true,
    trim: true,
  },
  description: {
    type: String,
    required: true,
  },
  price: {
    type: Number,
    required: true,
  },
  stockQuantity: {
    type: Number,
    required: true,
    min:0
  },
});

export const Product = mongoose.model("Product", ProductSchema);
